package com.pojo;

public class CoursesPoint {
    private int id;
    private int coursesId;
    private int userId;
    private int coursesPoint;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getCoursesId() {
        return coursesId;
    }

    public void setCoursesId(int coursesId) {
        this.coursesId = coursesId;
    }

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public int getCoursesPoint() {
        return coursesPoint;
    }

    public void setCoursesPoint(int coursesPoint) {
        this.coursesPoint = coursesPoint;
    }
}
